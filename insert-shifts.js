async function sleep(ms) { return new Promise(r => setTimeout(r, ms)); }

async function login() {
  const res = await fetch(`${BASE_URL}/api/auth/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ username: USERNAME, password: PASSWORD })
  });
  if (!res.ok) throw new Error(`Login failed: ${await res.text()}`);
  const data = await res.json();
  console.log(`Logged in as user ID: ${data.user.id}`);
  return data.token;
}

async function createShift(token, date, shift_type, hours, hourly_wage) {
  const body = { date, shift_type, hours };
  if (hourly_wage) body.hourly_wage = hourly_wage;
  const res = await fetch(`${BASE_URL}/api/shifts`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json', 'Authorization': `Bearer ${token}` },
    body: JSON.stringify(body)
  });
  if (!res.ok) throw new Error(`Create shift failed (${res.status}): ${await res.text()}`);
  return await res.json();
}

