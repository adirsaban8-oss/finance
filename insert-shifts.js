async function main() {
  console.log(`Total shifts to insert: ${shifts.length}`);

  console.log('\nLogging in...');
  let token;
  try {
    token = await login();
  } catch (e) {
    console.error(e.message);
    process.exit(1);
  }

  console.log('\nInserting shifts...');
  let inserted = 0;
  let failed = 0;

  for (const [date, hours, shift_type] of shifts) {
    try {
      await createShift(token, date, shift_type, hours);
      inserted++;
      if (inserted % 50 === 0) {
        console.log(`  Inserted ${inserted}/${shifts.length} shifts...`);
      }
      await sleep(100);
    } catch (e) {
      console.error(`  Failed: ${date} ${shift_type} (${hours}h): ${e.message}`);
      failed++;
      await sleep(500);
    }
  }

  console.log(`\nDone! ${inserted} inserted, ${failed} failed`);
}

main().catch(console.error);
