async function sleep(ms) { return new Promise(r => setTimeout(r, ms)); }

async function login() {
  const res = await fetch(`${BASE_URL}/api/auth/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ username: USERNAME, password: PASSWORD })
  });
  if (!res.ok) throw new Error(`Login failed: ${await res.text()}`);
  const data = await res.json();
  console.log(`Logged in as user ID: ${data.user.id}`);
  return data.token;
}

async function deleteAllShifts(token) {
  // Get all shifts (no month filter)
  const months = [];
  for (let y = 2024; y <= 2026; y++) {
    for (let m = 1; m <= 12; m++) {
      months.push(`${y}-${String(m).padStart(2,'0')}`);
    }
  }
  let total = 0;
  for (const month of months) {
    const res = await fetch(`${BASE_URL}/api/shifts?month=${month}`, {
      headers: { 'Authorization': `Bearer ${token}` }
    });
    if (!res.ok) continue;
    const shifts = await res.json();
    const arr = Array.isArray(shifts) ? shifts : (shifts.shifts || []);
    for (const s of arr) {
      await fetch(`${BASE_URL}/api/shifts/${s.id}`, {
        method: 'DELETE',
        headers: { 'Authorization': `Bearer ${token}` }
      });
      total++;
    }
  }
  console.log(`Deleted ${total} existing shifts`);
}

async function createShift(token, data) {
  const res = await fetch(`${BASE_URL}/api/shifts`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json', 'Authorization': `Bearer ${token}` },
    body: JSON.stringify(data)
  });
  if (!res.ok) throw new Error(`Failed (${res.status}): ${await res.text()}`);
  return await res.json();
}

