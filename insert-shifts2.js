async function main() {
  console.log(`Total shifts to insert: ${shifts.length}`);

  console.log('\nLogging in...');
  const token = await login();

  console.log('\nDeleting existing shifts...');
  await deleteAllShifts(token);

  console.log('\nInserting shifts with shift_hours...');
  let inserted = 0, failed = 0;

  for (const [date, shift_hours, hours, shift_type, description] of shifts) {
    try {
      await createShift(token, {
        date, shift_type, hours,
        shift_hours: shift_hours || null,
        description: description || null
      });
      inserted++;
      if (inserted % 50 === 0) console.log(`  ${inserted}/${shifts.length}...`);
      await sleep(80);
    } catch (e) {
      console.error(`  FAIL ${date} ${shift_type}: ${e.message}`);
      failed++;
    }
  }

  console.log(`\nDone! ${inserted} inserted, ${failed} failed`);
}

main().catch(console.error);
